function showpase(a) {
    for (let j = 0; j < tongtrang; j++) {
        document.getElementsByTagName("li")[j].classList.remove("pastecolor");
    }
    document.getElementsByClassName("phimvip_contener")[0].innerHTML = "";
    let start = (a - 1) * sophim;//00
    let end = a * sophim - 1;//7
    let text = "";
    for (let i = 0; i < phimvip.length; i++) {
        if (i >= start && i <= end) {
            text +=
                `
           <div class="phimvip_item">
           <a href="${phimvip[i].link}">
               <img src="${phimvip[i].img}">
               <P>${phimvip[i].name}</P>
           </a>
       </div>
           `

        } document.getElementsByClassName("phimvip_contener")[0].innerHTML = text;
    }
    document.getElementsByTagName("li")[a - 1].classList.add("pastecolor");
    sotrang = a;

}
